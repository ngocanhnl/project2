package com.javaweb.repository.entity;

public class BuildingEntity {
	private String name;
	private Integer numberOfBasementInteger;
	private String ward;
	private String street;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getNumberOfBasementInteger() {
		return numberOfBasementInteger;
	}
	public void setNumberOfBasementInteger(Integer numberOfBasementInteger) {
		this.numberOfBasementInteger = numberOfBasementInteger;
	}
	public String getWard() {
		return ward;
	}
	public void setWard(String ward) {
		this.ward = ward;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	
	
	
	
}
